# Variables
# Nos permite almacenar datos, cada uno tendra un nombre y un valor (nombre = valor)

## Tipos de Datos 

# String -> str
nombre = "Yoel"
apellido_paterno = "Aliaga"

# Numeros Enteros (Positivos y Negativos) -> int
edad =  28
