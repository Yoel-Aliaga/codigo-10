# Numeros Flotantes -> float
pi = 3.1415
precio_pan = 0.30

# Boolean (Verdadero / Falso) -> bool
# En javascript -> true y false
# En python -> True y False (Las primeras letras en mayusculas)
soltero = False
vivo = True

